    assert isinstance(val, float), f"Shout be float but was {type(val)}"
    val = ch2_no_avg.fwhm_data["C2"].get("FWHM-Y")
    assert isinstance(val, float), f"Shout be float but was {type(val)}"
    val = ch2_single.fwhm_data["C2"].get("FWHM-Y")
    assert isinstance(val, float), f"Shout be float but was {type(val)}"


